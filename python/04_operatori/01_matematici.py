# Variables
a = 15
b = 4

# Addition
print("Addition:", a + b)  

# Subtraction
print("Subtraction:", a - b) 

# Multiplication
print("Multiplication:", a * b)  

# Division
print("Division:", a / b) 

# Floor Division
print("Floor Division:", a // b)  

# Modulus
print("Modulus:", a % b) 

# Exponentiation
print("Exponentiation:", a ** b)

# Absolute Value (abs)
res = abs(-10) # Output: 10 (absolute value of -10)
print(res)

# Round (round)
res = round(3.14159, 2)  # Output: 3.14 (rounds to 2 decimal places)
print(res)