public class _01_Selezione {
    public static void main(String[] args) {
        int voto = 6;
        if (voto == 6) {
            System.out.println("Sufficiente");
        }

        int ora = 14;
        if (ora >= 12 && ora <= 17) {
            System.out.println("Buon pomeriggio");
        } else {
            System.out.println("Buona giornata");
        }
    }
}
